package com.st.studygroup.model;

public class StudyGroupApplyDto {
	
	private int APNO;
	private int MNO;
	private int SNO;
	private String AP_ID;
	private String AP_CONTENT;
	private String AP_STATUS;
	
	
	public int getAPNO() {
		return APNO;
	}
	public void setAPNO(int aPNO) {
		APNO = aPNO;
	}
	public int getMNO() {
		return MNO;
	}
	public void setMNO(int mNO) {
		MNO = mNO;
	}
	public int getSNO() {
		return SNO;
	}
	public void setSNO(int sNO) {
		SNO = sNO;
	}
	public String getAP_ID() {
		return AP_ID;
	}
	public void setAP_ID(String aP_ID) {
		AP_ID = aP_ID;
	}
	public String getAP_CONTENT() {
		return AP_CONTENT;
	}
	public void setAP_CONTENT(String aP_CONTENT) {
		AP_CONTENT = aP_CONTENT;
	}
	public String getAP_STATUS() {
		return AP_STATUS;
	}
	public void setAP_STATUS(String aP_STATUS) {
		AP_STATUS = aP_STATUS;
	}
	
	
	

}
